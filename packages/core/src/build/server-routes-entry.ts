import { writeFileSync } from "node:fs";
import { join, resolve } from "node:path";
import { ensureDir } from "./shared";

const INTERNAL_MODULE_PATH = resolve(import.meta.dir, "../internal.ts").replace(/\\/g, "/");
const RUNTIME_ENV_MODULE_PATH = resolve(import.meta.dir, "../runtime-env.ts").replace(/\\/g, "/");

export interface ServerRoutesEntryOptions {
  outDir: string;
  rootPath: string;
  routes: Array<{ mode: "ssr" | "ssg" | "isr"; path: string; pattern: string }>;
  serverEntry: string;
}

/**
 * Generates `server.ts` — an intermediate entry used to produce `server.js`.
 *
 * Equivalent to `_compile-entry.ts` but for disk (non-binary) production builds:
 * 1. Statically imports every page module so Bun bundles them into server.js
 * 2. Sets production mode and registers everything in a single CompileContext
 * 3. Imports server.ts to boot the app
 *
 * This file is intermediate: `adapter/bun.ts` runs `Bun.build()` on it to produce
 * the self-contained `server.js` bundle, then deletes this file.
 */
export function generateServerRoutesEntry(options: ServerRoutesEntryOptions): string {
  const { outDir, rootPath, routes, serverEntry } = options;
  ensureDir(outDir);

  const allModulePaths = [rootPath, ...routes.map((r) => r.path)];
  const moduleImports: string[] = [];
  const moduleEntries: string[] = [];

  for (let i = 0; i < allModulePaths.length; i++) {
    const absPath = (allModulePaths[i] as string).replace(/\\/g, "/");
    const varName = `_mod${i}`;
    moduleImports.push(`import * as ${varName} from ${JSON.stringify(absPath)};`);
    moduleEntries.push(`  ${JSON.stringify(absPath)}: ${varName},`);
  }

  const routeEntries = routes.map(
    (r) =>
      `    { pattern: ${JSON.stringify(r.pattern)}, path: ${JSON.stringify(r.path.replace(/\\/g, "/"))}, mode: ${JSON.stringify(r.mode)} },`
  );

  const lines = [
    "// Auto-generated by elyra build — do not edit",
    `import { __setCompileContext } from ${JSON.stringify(INTERNAL_MODULE_PATH)};`,
    `import { __setDevMode } from ${JSON.stringify(RUNTIME_ENV_MODULE_PATH)};`,
    ...moduleImports,
    "",
    "// Force production mode — Bun may inline process.env.NODE_ENV at bundle time.",
    "__setDevMode(false);",
    'process.env.NODE_ENV = "production";',
    'process.env.ELYRA_BUILD_TARGET = "bun";',
    "",
    "__setCompileContext({",
    `  rootPath: ${JSON.stringify(rootPath.replace(/\\/g, "/"))},`,
    "  modules: {",
    ...moduleEntries,
    "  },",
    "  routes: [",
    ...routeEntries,
    "  ],",
    "});",
    "",
    `await import(${JSON.stringify(serverEntry.replace(/\\/g, "/"))});`,
    "",
  ];

  // Named "server.ts" so Bun.build() outputs "server.js"
  const entryPath = join(outDir, "server.ts");
  writeFileSync(entryPath, lines.join("\n"));
  return entryPath;
}
